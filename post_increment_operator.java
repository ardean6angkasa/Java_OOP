public class post_increment_operator {
    public static void main(String[] args) {
        int a;
        int i = 0;
        a = i++;
        int b;
        int j = 0;
        b = j++;
        int c;
        int k = 0;
        c = k++;
        System.out.println("a = " + a+" b = " + b+" c = " + c);
        System.out.println("i = " + i + ", j= " + j + ", k= " + k);
    }
}
